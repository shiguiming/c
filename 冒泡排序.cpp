#include<stdio.h>//今天写一个冒泡排序在家听歌慢慢写
int main()//来一个主函数
{
	int a[10] = { 4,7,2,9,12,20,99,83,27,66};//随便定义一个数组输入几个数
	int i,k,t;//定义循环变量和中间变量
	{for (i = 0; i < 10; i++)//第一个循环控制循环次数
		for (k = 9; k >= i; k--)//第二个循环用来交换次序
			if (a[k] < a[k - 1])//交换次序不断循环让小的数往前靠大的数往后靠
			{
				t = a[k - 1];
				a[k - 1] = a[k];//交换数字
				a[k] = t;
			}
	}
	for (i = 0; i < 10; i++)//循环输出已经排好的数字结束
		printf("%3d", a[i]);
}